<?php

// 1 задание
$n = 100;
$array[]= [];

for ($i = 0; $i < $n; $i++) { // 100
    for ($j = 1; $j < $n; $j *= 2) { // 7
        $array[$i][$j]= true;
    } }
// O(n log n), 700 итераций

